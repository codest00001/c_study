CREATE TABLE 학생  -- 학생 테이블 생성
(이름 VARCHAR(50) NOT NULL,
학번 CHAR(8),
전공 CHAR(5),
성별 CHAR(1) DEFAULT '남' -- 성별 기본값 설정
CHECK (성별 IN ('남','여')), -- 성별 도메인 설정
생년월일 DATE,
PRIMARY KEY (학번), -- 기본키 설정
FOREIGN KEY (전공) REFERENCES 학과(학과코드)); -- 외래키 설정

